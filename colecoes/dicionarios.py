#conjuntos - unico e imutável
conjunto = set([4, 7, 2, 3, 0, 8])
print(conjunto)


#tupla - unica e imutável também
tupla = (3, 2, 4, 5, 6)
print(tupla)

#dicionarios - várias informações de uma mesma coisa
pessoas = ['Orion', 'Maria', 'João']
print(pessoas)

pessoa = {'nome':'Orion', 'telefone':'(93)9999-9999', 'endereço':'ABC'}
print(pessoa)

